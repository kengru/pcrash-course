# Exercise 3-8. Sorting and printing a list.

places = ['Paris', 'Berlin', 'Hong Kong', 'London', 'Singapore']

print(places)
print(sorted(places))
print(places)
print(sorted(places, reverse=True))
print(places)

places.reverse()
print(places)
places.reverse()
print(places)
places.sort()
print(places)
places.sort(reverse=True)
print(places)
