# Exercise 5-1. Writing predictions of ifs.

car = 'audi'
print('Is the car an subaru? I say it isnt.')
print(car == 'subaru')
print('Is the car an audi? I say it is.')
print(car == 'audi')

pizza = 'pepperoni'
print('Is the pizza a pepperoni one? I say it is.')
print(pizza == 'pepperoni')
print('Is the pizza a cheese one tho? I say it isnt')
print(pizza == 'cheese')

home = 'apartment'
print('Is home a house? I think it isnt.')
print(home == 'home')
print('Is home an apartment? Probably it is.')
print(home == 'apartment')
